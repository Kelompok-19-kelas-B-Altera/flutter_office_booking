class UserModel {
  final int uID;
  final String username;
  final String email;
  final String password;
  final String fullName;
  final String phone;
  final String role;

  UserModel({
    required this.uID,
    required this.username,
    required this.email,
    required this.password,
    required this.fullName,
    required this.phone,
    required this.role,
  });
}
